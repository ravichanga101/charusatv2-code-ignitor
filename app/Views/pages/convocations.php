                                                <h5>Convocations</h5>
                                                <p>
                                                    Convocation marks the culmination of the academic pursuits. 
                                                    Students, thereafter, move on to higher studies or look for / 
                                                    join jobs of their interest. At CHARUSAT, Convocation is organized 
                                                    with all piousness and Indian tradition.
                                                </p>
                                                <p>
                                                    Importantly, at CHARUSAT, all the students are conferred upon the 
                                                    Degrees in person in a dignified manner. Today, when there is the 
                                                    tradition of giving Gold Plated Medals, all the Medals conferred 
                                                    by CHARUSAT are real Gold Medals with certificates. Students and 
                                                    their Parents are provided with hospitality and special attention, 
                                                    too. Exceptionally, the Convocation at CHARUSAT represents the 
                                                    serenity of Indian Culture. The Procession is fragranced with the
                                                    Ganpati Vandana and Degree Conferment with Vedic Shlokas in addition 
                                                    to the Indian costumes and Deep Pragatya to Inaugurate the Convocation.
                                                </p>
                                                <p>
                                                    Uniquely, dignitaries with substantial contribution to the National 
                                                    Development have been blessing the Convocations at CHARUSAT. 
                                                    So far, 4 Convocations have been organized at CHARUSAT. Glimpse are:
                                                </p>
                                                
