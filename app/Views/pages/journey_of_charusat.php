                                                <h5>The Journey</h5>
                                                    <p style="text-align: justify;">
                                                        Charotar University of Science and Technology – CHARUSAT has 
                                                        been conceived by Shri Charotar Moti Sattavis Patidar Kelavani 
                                                        Mandal to put India on global education map by making <strong>Charotar
                                                        – the Land of Sardar Patel, a Global Education Hub.</strong>
                                                    </p>
                                                    <p style="text-align: justify;">
                                                        <strong>Kelavani Mandal</strong>, established in 1994,
                                                        is a non-profit premier trust of India that works with the aim 
                                                        to provide social service through education. The organization
                                                        has a social lineage of more than 125 years. The parent organization,
                                                        <strong>Shri Charotar Moti Sattavis Leuva Patidar Samaj– Matrusanstha</strong>-
                                                        is known for the social revolution it brought about by initiating Mass Marriages
                                                        in Patidar community on a massive scale.
                                                    </p>
                                                    <p style="text-align: justify;">
                                                        Kelavani Mandal has been established with the distinct objective of 
                                                        creating state-of-the-art educational facilities. The trust is a 
                                                        democratically managed institution known for its transparent functioning
                                                        and integrity.
                                                    </p>
                                                    <p style="text-align: justify;">
                                                        The Kelavani Mandal is the sponsoring body of 
                                                        CHARUSAT. The development path of Kelavani Mandal was illuminated under the 
                                                        leadership of two former Presidents: Late <strong>Shri Chhotabhai Bhikhabhai 
                                                        Patel</strong> and Late <strong>Dr. K C Patel</strong>. Late Shri Chhotabhai 
                                                        Bhikhabhai Patel was an illustrious businessman and social leader based in Mumbai.
                                                        This founder President of Kelavani Mandal always insisted on setting Global Standards.
                                                        Late Dr. K C Patel, a renowned nuclear scientist and a prominent educationist of
                                                        Gujarat, succeeded Shri Chhotabhai Bhikhabhai Patel, and dreamt of a university 
                                                        at <strong>Education Campus, Changa</strong> (ECC).
                                                    </p>
                                                    <p>
                                                        <img src = "upload/Torch bearers.jpg" />
                                                    </p>
                                                    <h5>ECC TO CHARUSAT</h5>
                                                    
                                                    <p style="text-align: justify;">
                                                        The journey at Education Campus – Changa 
                                                        (now <strong>CHARUSAT Campus</strong>) started as a single 
                                                        Institute in the year 2000 with the establishment of <strong>
                                                            Charotar Institute of Technology - Changa (CITC)</strong>.
                                                            It was the first institute and now it is known as <strong>
                                                                Chandubhai S Patel Institute of Technology (CSPIT)
                                                                </strong>. The journey began with only with 240 students,
                                                                4 programmes, 1 institute and an investment of Rs. 3 Crores.
                                                                Presently, CHARUSAT offers more than 72 Programs at 9 
                                                                different institutes under the tutelage of 6 Faculties. 
                                                                CHARUSAT has a student strength of 7500, employee strength
                                                                of 550 and a capital outlay of Rs. 125 Crores.</p>
                                                    <p style="text-align: justify;">
                                                        Year after year institutes and programs were added. In the years 
                                                        2002 and 2004, the Mandal established <strong>Charotar Institute 
                                                        of Computer Applications (CICA)</strong> and <strong>Ramanbhai 
                                                        Patel College of Pharmacy (RPCP)</strong> respectively. CICA 
                                                        has been rechristened as <strong>Smt. Chandaben Mohanbhai Patel 
                                                        Institute of Computer Applications (CMPICA)</strong>. The year 
                                                        2006 saw the commencement of <strong>Indukaka Ipcowala Institute 
                                                        of Management (I2IM)</strong>. Two more institutes <strong>P D 
                                                        Patel Institute of Applied Sciences (PDPIAS)</strong> and <strong>
                                                            Dr. K C Patel R &amp; D Centre </strong>were established in 
                                                            the year 2007.</p>
                                                    <p style="text-align: justify;">
                                                        Continuing the growth, in the year 2009, two more institutes 
                                                        were set up – Charotar Institute of Physiotherapy and Charotar 
                                                        Institute of Nursing which were instrumental in heralding the 
                                                        entry in the field of Healthcare Education. Both the institutes 
                                                        are rechristened. They are now called <strong>Ashok and Rita 
                                                        Patel Institute of Physiotherapy (ARIP)</strong> and <strong>
                                                            Manikaka Topawala Institute of Nursing (MTIN)</strong>.</p>
                                                    <p style="text-align: justify;">In spite of the growth and 
                                                    achievements, there was a sense of dissatisfaction within the ECC 
                                                    system about the poor fit between the available curricula for the 
                                                    graduating students and the skills &amp; knowledge expected of the
                                                    graduates by the industry. The need of the hour was academic freedom 
                                                    whereby novel curricula can replace the obsolete ones matching the 
                                                    requirements of the Industry. Having own university, thus, became 
                                                    a necessity.</p>
                                                    <p style="text-align: justify;">
                                                        An application was made to University Grants Commission (UGC) 
                                                        for acquiring the status of a “Deemed to be University”. Similar 
                                                        application was made to Government of Gujarat for the establishment
                                                        of a State Private University. The request made to the Government
                                                        of Gujarat started getting materialized. Team CHARUSAT was invited 
                                                        to make a presentation before the High Power Committee. And finally,
                                                        on July 7, 2009, with due efforts and procedures, the conglomerate 
                                                        of Institutes and Programs at Education Campus, Changa, was 
                                                        accorded the status of a University. it was named as <strong>
                                                            Charotar University of Science and Technology (CHARUSAT)
                                                            </strong>. The name Charotar commemorates the region Charotar-
                                                            the golden land.
                                                    </p>
                                                    <p style="text-align: justify;">
                                                        CHARUSAT , having accorded the status of a university under 
                                                        Gujarat State Act No. 8 of 2009, is also empowered to award 
                                                        degrees by UGC under Section 22 of the UGC Act, 1956.
                                                    </p>
                                                    <p style="text-align: justify;">
                                                        The shadow structure with Academic Council, Faculty Boards, 
                                                        Board of Studies, Curricula, Syllabi was ready beforehand. 
                                                        In the transition phase and ingraining phase, President 
                                                        <strong>Shri Surendra Patel</strong> and Secretary <strong>
                                                            Dr. M C Patel</strong> of Kelavani Mandal provided honorary 
                                                            services as the President and the Provost of CHARUSAT 
                                                            respectively.
                                                        <br> Surendra Patel, fondly called Kaka, is also the incumbent 
                                                        President of Charusat.
                                                        <br> After a brief stint as founding Provost, Dr M C Patel 
                                                        continues to render his voluntary services as a Secretary of 
                                                        Kelavani Mandal.
                                                    </p>
                                                    
                                                
                                                
                                                
                                                
                                                
                                                
                                                
