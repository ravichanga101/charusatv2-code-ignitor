                                                <h5>ACTIVITIES OF CDPC</h5>
                                                <h6>Training Activities and Interview Preparation</h6>
                                                <p>At CHARUSAT, you will get assistance for your summer internship and final year internship for in leading government or private organizations, R&D and NGOs.</p>
                                                
                                                <h6>Mock Placement Drive</h6>
                                                <p>Be it resume building, aptitude training, preparing for group discussion and interview; we will leave no stone unturned to gear you up for the placement.</p>
                                                
                                                <h6>Placement Activities</h6>
                                                <p>CHARUSAT has a history of not only higher placement percentage but also placement in leading industries and organizations. With our structured placement and training programme which includes mock placement drive, aptitude training and more, we help you bag your dream job through on-campus/off-campus recruitments.</p>
                                                
                                                <h6>Training Activities</h6>
                                                <p>The training programs offered include Behavioral Skills, Technical Skills, Personality Development, and Communication Skills.</p>
                                                
                                                <h6>Higher Education Helpdesk</h6>
                                                <p>At CHARUSAT, we organize USA/UK Education fair to help you connect directly to the foreign university representatives and get the information you seek. Moreover, be it assistance with course selection, qualifying tests (GATE/GRE/TOEFL/CAT), letter of recommendation, statement of purpose, document verification or application process and visa, you will receive all required assistance.</p>
                                                
                                                <h6>Career Guidance</h6>
                                                <p>We will prepare you for various career avenues and options like jobs in PSU by offering you training and guidance for UPSC, GPSC etc. We also encourage students to join Armed forces and conduct seminar or lectures by experts from Navy, NCC, IAS/IPS officers, etc.</p>
