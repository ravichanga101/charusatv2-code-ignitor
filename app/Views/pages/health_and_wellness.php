                                <h5>GENERAL GUIDELINES:</h5>
                                <p>A broad-based ‘Student Counselling System’ is being envisaged which shall be,
                                    interactive and result-oriented, involving students, teachers and parents,
                                    addressing common student concerns ranging from anxiety, stress, fear of change and
                                    failure, homesickness and a slew of academic worries.</p>

                                <p>In view to meet the above:</p>
                                <ul>
                                    <li>
                                        All the Principals/ HoDs (Engg.) shall have to allocate counsellor teachers to
                                        the students at the beginning of the new Term. This has to be widely notified to
                                        the students and parents through display on the notice boards, emails, etc.
                                        within a week of the beginning of the Term.
                                    </li>
                                    <li>Each teacher will act as a Counsellor with a batch ranging from 15 to 25
                                        students allotted to him/her.</li>
                                    <li>
                                        Students allocated to a particular counsellor will remain under his/her guidance
                                        till the completion of their studies (UG/PG Programmes). In the event of Teacher
                                        Counsellor leaving the University, other Teacher Counsellor may be allocated to
                                        the students by Principal immediately.
                                    </li>
                                    <li>The Teacher Counsellor is expected to undertake and execute:</li>

                                    <li>The Teacher Counsellor will need to have a thorough understanding of the credit system/ evaluation system for counselling a student for academic as well as other matters like:</li>
                                    <li>The Counselling should be undertaken in a planned manner to ensure continuity and consistency.</li>
                                    <li>The counsellors shall keep the parents of their counsellees informed regularly at a maximum interval of one month.</li>
                                    <li>The counsellors shall report the progress of their counsellees to the Department Counselling Coordinator every month.</li>
                                    <li>The counselling activities shall be part of Performance Appraisals of the teachers.</li>
                                    <li>The Principals shall have to regularly monitor the activities of Student Counselling and report to Academic Counsel in its meetings.</li>
                                </ul>
                                <h6>Note:</h6>
                                <ul>
                                    <li>An Online Test will be initiated and coordinated by HRDC for the Faculty Members to evaluate and ensure their thorough understanding of Credit Systems/ Evaluation System and other related academic matters, for the purpose of effective counselling.</li>
                                    <li>To equip the Teacher Counsellor with techniques of counselling, the teacher counsellor shall be provided advanced training under Senior Professional Counsellor.</li>
                                </ul>

                                <h5>COUNSELLING STRUCTURE:</h5>
                                <p>The Counselling Cell shall comprise of the following members:</p>
                                <img src = "upload/swp.png" />
                                <p>*Note: For Institutes other than CSPIT, “Principal” shall be the “Institute Counsellor”</p>
                                <p>
                                    <b>Teacher Counsellor</b> – Faculty members
                                    <ul>
                                        <li>One Teacher Counsellor will be allocated to 15 to 25 students for counselling. (Please refer General Guidelines for details)</li>
                                        <li>Teacher Counsellor will report to Department Counselling Coordinator twice in every semester through Mentor Report.</li>
                                        <li>For students requiring immediate attention and remedial action, Teacher Counsellor shall immediately refer the said students directly to Institute Counsellor {Principal/HoD (Engg.)}</li>
                                    
                                    </ul>
                                </p>
                                <p>
                                    <b>Department Counselling Co-ordinator</b> – A Senior Faculty, having more than 05 years of teaching experience will be the Department Counselling Co-ordinator.
                                    <ul>
                                        <li>Department Counselling Co-ordinator will look after the counselling at departmental level and coordinate with the Teacher Counsellors for continuous feedback on the progress of the students twice in a semester.</li>
                                        <li>Department Counselling Co-ordinator, having proven track record of effective teaching and counselling, shall also undertake the responsibility of meeting once in a month and guiding the “Potential Learners” and “Progressive Learners”, as referred by different Teacher Counsellors.</li>
                                        <li>Considering the number of students referred by the Teacher Counsellors, additional Department Counselling Co-ordinators may be assigned by the Institute Counsellor.</li>
                                        <li>The Department Counselling Coordinator shall report the progress made by the counselled students to Institute Counsellor {Principal/HoD (Engg.)} for necessary actions, wherever needed, once in every quarter.</li>
                                        
                                    </ul>
                                </p>
                                <p>
                                    <b>Institute Counsellor</b> - The Head of the Department will be the Institute Counsellor. For Institutes other than CSPIT, “Principal” shall be the “Institute Counsellor”.
                                    <ul>
                                        <li>The Institute Counsellor will oversee the progress and the noticeable issues and shall take immediate remedial actions. (Please refer ‘Counselling System: Functional Flow’ for further guidance)</li>
                                    </ul>
                                </p>
                                <p>
                                    <b>Professional Member </b> – A Professional Psychologist and Psycho Therapist, appointed under Student Wellness Programme, or Senior Psychologist, depending on gravity of the circumstance, will be the Professional Member.

                                </p>
                                
                                
                                <p>
                                    <h5>COUNSELLING SYSTEM: FUNCTIONAL FLOW</h5>
                                    <ol>
                                        <li>As soon as a student gets enrolled and begins his/her sessions, he/she will be assigned to a particular Teacher Counsellor by the Principal/ HoD (Engg.).</li>
                                        <li>It will be a Teacher Counsellor’s utmost responsibility to ensure that the new incumbent is comfortable in the new environment and is getting acclimatized. The illustrative model could be through building a rapport with the students by way of different activities, games and informal interactions carried out to help build strong Teacher-Taught relationship.</li>
                                        <li>The Counsellor should ensure that all the Counsellees under him/her have filled in all their personal details on e-governance of the University within two weeks of beginning of the Term.</li>
                                        <li>The counsellors shall have a continuous observation of the academic performance, relation with the peers, behavior patterns and psychological conditions of their counsellees, so as to be able to track any irregularity at an earlier stage and suggest corrective actions.</li>
                                        <li>There upon, the counsellors shall conduct meeting, once in a fortnight, with their counselleess, so as to be able to keep a track of their progress in academic, social and personal fronts. These meets need to be recorded with signature of the Counsellor as well as the Counsellee.</li>
                                        <li>SWP Counsellor conducts “Group Interaction Sessions” to assess the overall wellness level of the students. SWP Counsellor gets the “Student Wellness Questionnaire” filled up by the students to ascertain the patterns of further counselling support required.</li>
                                        <p>The Questionnaire is based on psychometric principles to measure key aspects of human personality namely:</p>
                                    <ul>
                                        <li>Positive Personal Traits</li>
                                        <li>Dark Personal Traits</li>
                                        <li>Motivation, Values, Preferences</li>
                                        <li>Cognitive Ability</li>
                                        <li>Emotional Stability</li>
                                        <li>Interpersonal capabilities</li>
                                        <li>Addiction</li>
                                        <li>Confidence </li>
                                        <li>Concentration</li>
                                        <li>Study Habits</li>
                                    </ul>
                                    <p>
                                        On critical appraisal of the student wellness questionnaire students are grouped under 3-Tier Systems of Intervention:
                                    </p>
                                    <p>
                                        <b>Tier-I:</b>
                                        Students falling under this group are potential learners with mild to moderate level of immaturity. Hence, there arises the need for regular guidance driven Intervention by their respective Teacher Mentors.
                                    </p>
                                    <p>
                                        <b>Tier-II:</b>
                                        Students falling under this group are capable lot, but lacking motivation, indifferent, inattentive and passive in their approach to studies. Hence, there arises the need for Individual Intervention for motivation and support.
                                    </p>    
                                    <p>
                                        <b>Tier-III:</b>
                                        Students falling under this group are “Students at Risk”. Hence, there arises the need for One-to-One Therapeutic Intervention by SWP Counselor.
                                    </p>
                                    
                                        A Quarterly Comprehensive Report has to be prepared by SWP Counsellor, putting together the progress reports of all ‘Three Tiers’, and to be submitted to HRDC which in turn will be forwarded to the respective Institute Counsellor {Principal/HoD(Engg.)}.
                                    
                                        <li>
                                            <p>After completion of a Semester and announcement of results of the students, the Teacher Counsellor will segregate their respective counsellees on the basis of academic Performance as under:</p>
                                                <ul>
                                                    <li><strong>Potential Learners</strong>: CGPA &lt; 5</li>
                                                    <li><strong>Progressive Learners</strong>: CGPA 5 to 7</li>
                                                    <li><strong>Proficient Learners</strong>: CGPA above 7</li>
                                                </ul>
                                        </li>
                                        <li>
                                            Herein, the Teacher Counsellor should arrange for frequent sessions with the <strong>‘Potential Learners’</strong> and <strong>‘Progressive Learners’</strong> and guide them to focus on clearing the backlogs first, if any, and subsequently endeavour towards improving CGPA/SGPA.
                                        </li>
                                        <li>
                                            Further, the Teacher Counsellor shall refer the <strong>‘Potential Learners’</strong> and <strong>‘Progressive Learners’</strong> to Department Counselling Co-ordinators, once in a month as outlined under Counselling Structure – Department Counselling Co-ordinator.
                                        </li>
                                        <li>
                                            In order to achieve students’ optimal development and well-being, a Parent-Teacher’s meet, with parents of <strong>‘Potential Learners’</strong> as well as <strong>‘Progressive Learners’</strong>, shall be organized after a month of announcement of results, so as to enable the Teacher Counsellors and the Department Counselling Co-ordinators to convey the progress made by the students to their parents. 
                                        </li>
                                        <li>
                                            The <strong>‘Proficient Learners’</strong> having academic excellence should be encouraged and guided by the Institute Counsellors for greater participation in Inter-University events and activities, State/ National level competitions/Workshop/Seminars, etc.. Such exposure will ultimately contribute towards holistic growth of the student.
                                        </li>
                                        
                                    </ol>
                                    
                                    
                                    <h5>NEW INITIATIVES OF SWP</h5>
                                    <p><strong>SWP REACH Programmme:</strong><br>
                                        </p><ul>
                                            <li>HRDC introduced SWP Reach Programme wherein SWP Councellor’s Service has been made available at the respective institute on every Tuesday and first Friday of the month.</li>
                                            <li>The objective of Reach Program is to reach out to maximum Students and their Mentors as well as to the Parents, wherever need be, through compassionate therapeutic interventions, so as to promote renewed hopes, personal fulfillment and academic enrichment.</li>
                                        </ul>
                                    <p></p>
                                    <p><strong>Student Happiness and Progression Survey:</strong><br>
                                        </p><ul>
                                            <li>In line with SWP Group Sessions, HRDC proposes to initiate a Survey: Phase 2 under the caption ‘Student Happiness and Progression Survey’ in order to get a feel of the First Year Student’s Academic and Emotional bonding with University.</li>
                                            <li>The objective is to be in constant touch with student’s need and the improvement required there upon. This will also serve as secondary data for NAAC criteria under Student Satisfaction Survey.</li>
                                        </ul>
                                    <p></p>
                                    <p><strong>SWP Research Approach towards Psycho-Social Elements:</strong><br>
                                        </p><ul>
                                            <li>HRDC proposes under SWP Activity, to explore how formulating interventions on the basis of Psycho-Social factors offer an avenue for student’s to address specific attitudes, emotions, and behaviors that relate to college success.</li>
                                        </ul>
