                                                        <h5>Objective of Committee:</h5>
                                                        <p>
                                                            
                                                            The main objectives are to create and maintain a disabled-friendly
                                                            campus for persons withdisabilities who are studying or 
                                                            working in the University and to address their concerns
                                                            and requirements. The Committee shall consist of teachers, 
                                                            non-teaching staff, students with 
                                                            disability and parents from various institute.
                                                        </p>
                                                        <h5>Role of the committee:</h5>
                                                        <p>
                                                            <ul>
                                                                <li>
                                                                    To develop procedures to identify and provide 
                                                                    academic support to students with disabilities and 
                                                                    support services (such as use of readers; scribes in
                                                                    examination) and reexamination for students
                                                                    with such difficulties in accordance with the 
                                                                    University Grants commission guidelines.
                                                                </li>
                                                                <li>
                                                                    To create a student volunteer base to undertake various
                                                                    academic support activities for students with disabilities.
                                                                </li>
                                                                <li>
                                                                    To examine student requests, consulting with medical 
                                                                    practitioner at the Institutes.
                                                                </li>
                                                                <li>
                                                                    Protect the confidentiality of students’ disability – related information.
                                                                </li>
                                                                <li>
                                                                    Keeping the copies of persons with Disabilities Act and 
                                                                    Rules, UGC guidelines, Government of Gujarat guidelines 
                                                                    and make appropriate recommendations for their effective 
                                                                    implementation.

                                                                </li>
                                                                
                                                            </ul>
                                                            </p>
                                                            <p>
                                                                <h5>
                                                                    Frequency of Meetings
                                                                </h5>
                                                                <ul>
                                                                    <li>
                                                                        The Committee shall meet at least once in a semester 
                                                                        or as often as may be needed.
                                                                    </li>
                                                                    <li>
                                                                        The Committee can co-opt other faculty members, non-teaching staff and students.
                                                                    </li>
                                                                </ul>
                                                            
