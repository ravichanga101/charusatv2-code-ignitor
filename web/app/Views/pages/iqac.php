                                        <h5>Internal Quality Assurance Cell (IQAC)</h5>            
                                        <p>
                                            The Internal Quality Assurance Cell (IQAC) has been constituted by CHARUSAT
                                            since December 2013. The cell has been entrusted with the responsibility 
                                            of getting accreditation for CHARUSAT from National Assessment and 
                                            Accreditation Council (NAAC). It further envisages instilling a momentum of 
                                            quality consciousness amongst the constituent institutes of CHARUSAT, aiming 
                                            for continuous improvement. IQAC would strive to develop a culture of quality 
                                            on the campus as well as enhancing the awareness of Institutional Quality 
                                            Assurance with all the stakeholders.
                                        </p>
                                        <p>
                                            NAAC accreditation process has to be initiated after 5 years of establishment 
                                            of the University as per the act of CHARUSAT. Further, as per the UGC directives,
                                            all institutes of higher education should undertake the accreditation process
                                            before 1st June 2015.
                                        </p>
