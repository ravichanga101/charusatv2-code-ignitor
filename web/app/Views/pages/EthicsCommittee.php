                                            <div class="gdlr-core-text-box-item-content" style="font-size: 16px ;text-transform: none ;">
                                                <p>The Institutional Ethic Committee-CHARUSAT (IEC-CHARUSAT) is a formally designated independent entity which reviews, approves and monitors all research projects involving human participants. The primary aim of the IEC-CHARUSAT is to protect the rights and welfare of all the participants of the biomedical /clinical research.</p>

                                                <p>The mandate of IEC-CHARUSAT is to ensure participants be exposed to minimum risk or no risk i.e which may be anticipated as harm or discomfort not greater than that encountered in routine daily life activities of the general population or during the performance of routine physical or psychological examination, procedure or tests.</p>

                                                <p>The mandate of IEC-CHARUSAT is to ensure the volunteer participation, dignity, rights, safety and well being of participants during the biomedical or clinical study and to provide transparent assurance of protection that the research is carried out upholding the ethical values. IEC-CHARUSAT will keep all emerging ethical issues keeping in the view of social, cultural, economic, legal and religious concerns of the society. Ethics is a subject of discussions and debates and each and every word and line will be deliberated by the IEC-CHARUSAT.</p>

                                                <p>Ethical Clinical research and practice must respect all four of the basic prima facie moral commitments: autonomy, justice, beneficence and Non-maleficence - plus concern for their scope of application. These “four principles plus scope” approach provides a simple, accessible, and culturally neutral approach for considering ethical issues.</p>

                                                <p>IEC-CHARUSAT is constituted in line of ICMR guideline 2017 for National Ethical Guidelines for Biomedical and Health Research involving Human Participants, and registered successfully with Department of Health and Research (DHR), Govt. of India, New Delhi, Central Drugs Standard Control Organisation (CDSCO), Govt. of India, New Delhi and U.S. Department of Health and Human Services (HHS) & Registration of an Institutional Review Board (IRB).</p>
